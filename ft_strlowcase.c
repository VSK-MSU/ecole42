char	ft_strlowcase(char *str)
{
	char *timed;

	timed = str;
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			*str += 'a' - 'A';
		}
		str += 1;
	}
	return (timed);
}
