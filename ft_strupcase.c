char	*ft_strupcase(char *str)
{
	char *timed;

	timed = str;
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z')
		{
			*str -= 'a' - 'A';
		}
		str += 1;
	}
	return (timed);
}
